#include <iostream>
// iostream is a header file that contains function for basic input and output operations

// main() function is where the program will began
int main() {
    std::cout << "Hello World" << std::endl;
    // std::cout = standard character output
    // std::endl = for new line
    std::cout << "It's really good" << '\n';
    // '\n' creates a new line
    return 0;
}
